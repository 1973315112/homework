void Forward_elimination()//前向消元
{
	for(int I=1;I<=N;I++)
	{
		if(A[I][0]==0)
		{
			for(int i=1;i<=n;i++)
			{
				if(B[i][0]==0||B[i][I]==0) continue;
				A[I][0]=1;
				B[i][0]=0;
				for(int j=I;j<=N;j++)
				{
					A[I][j]=B[i][j];
				}				
				break;
			}			
		}
		if(A[I][0]==0) continue;
		for(int i=1;i<=n;i++)
		{
			if(B[i][0]==0||B[i][I]==0) continue;
			for(int j=I;j<=N;j++)
			{
				B[i][j]=B[i][j]^A[I][j];
			}
		}
	}
}

